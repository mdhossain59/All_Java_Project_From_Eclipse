package JavaLearningExercise;

public class ExampleOfWhileLoop {
	public static void main(String[] args) {
	
		int waterPot =0;
		int waterCup = 0;
		while (waterCup <10) {
			
			waterPot = waterPot + 1;
			waterCup++;
			System.out.println("total water in the pot is "+waterPot);
		}
		
		
		
	}
}
